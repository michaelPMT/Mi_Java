    public void Agrega(){
        
        String Datos[] = new String[10];
        i++;
        Datos[0] = cod+i;
        Datos[1] = txtNombre.getText();
        Datos[2] = txtApellidos.getText();
        Datos[3] = txtDNI.getText();
        Datos[4] = obtenerFecha();
        Datos[5] = txtDireccion.getText();
        Datos[6] = txtTelef.getText();
        Datos[7] = (String) cmbGrados.getSelectedItem();
        Datos[8] = obtenerSeccion();
        JOptionPane.showMessageDialog(rootPane, "Alumno Agregado Corecctamente! ","Nuevo Mensaje",JOptionPane.OK_OPTION);
        modelo.addRow(Datos);
        
        
    }
    
    public void Eliminar(){
        int filaSeleccionada = tabladatos.getSelectedRow();
        if(filaSeleccionada >= 0){
            modelo.removeRow(filaSeleccionada);
            JOptionPane.showMessageDialog(null, "Alumno Eliminado Corecctamente! ");
        }
        else{
            JOptionPane.showMessageDialog(null,"Tabla Vacía o No seleccionó ninguna Fila");
        }
    }
    
    public void EliminarTodo(){
        int cantidadFilas = tabladatos.getRowCount();
        for(int i = cantidadFilas-1; i>=0;i--){
            modelo.removeRow(i);
        }
    }
    
    
    public void filtro() {
        int columnaABuscar = 0;
        if (cmbBuscar.getSelectedItem() == "Codigo") {
            columnaABuscar = 0;
        }
        if (cmbBuscar.getSelectedItem().toString() == "Nombres") {
            columnaABuscar = 1;
        }
        if (cmbBuscar.getSelectedItem() == "Apellidos") {
            columnaABuscar = 2;
        }
        if (cmbBuscar.getSelectedItem() == "DNI") {
            columnaABuscar = 3;
        }
        trsFiltro.setRowFilter(RowFilter.regexFilter(txtBuscar.getText(), columnaABuscar));
    }
    
