    public String obtenerFecha(){
        String formato = jDateFecha.getDateFormatString();
         Date date = jDateFecha.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat(formato);
        String factual = String.valueOf(sdf.format(date));
        
        return factual;
    }
    
    public String obtenerSeccion(){
        String sec="";
        if(rbtA.isSelected())sec = "A";
        else if(rbtB.isSelected())sec = "B";
        else if(rbtC.isSelected())sec = "C";

        return sec;
    }

